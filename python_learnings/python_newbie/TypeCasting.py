#Types
print("\ntype()")
print(type({})) 
print(type([])) 
print(type(21)) 
print(type(2.1))
print(type(True)) 

# Type casting 
# float(element)
print("\nfloat()")
print(float(121))
print(float("01"))

print("\nint()")

# int(element)
print(int(True))
print(int(False))
print(int(3.43))
print(type(int("100")))

# bool(element)
print("\nbool()")
print(bool(2.1)) 
print(bool(1))
print(bool(0))
print(bool("1"))
print(bool("0"))
print(bool("Karthik"))
print(bool(2.1))

# str(element)
print("\nstr()")
print(str(1))
print(str(21.2))
print(str([1,21])) #<class 'str'>
print(type([1,2,"3"])) #<class 'list'>
print(str(True))
